#include "monty.h"

/**
 *  f_add - This is a function that adds the top two elements of the stack
 *  @head: Double pointer head to the stack
 *  @counter: A line count
 *
 * Return: void
 */

void f_add(stack_t **head, unsigned int counter)
{
	stack_t *h;
	int length = 0, temp;

	h = *head;
	while (h)
	{
		h = h->next;
		length++;
	}
	if (length < 2)
	{
		fprintf(stderr, "L%d: can't add, stack too short\n", counter);
		fclose(bus.file);
		free(bus.content);
		free_stack(*head);
		exit(EXIT_FAILURE);
	}
	h = *head;
	temp = h->n + h->next->n;
	h->next->n = temp;
	*head = h->next;
	free(h);
}

/**
 * f_sub - This is a function that substracts nodes
 * @head: A double head pointer to the stack
 * @counter: A line count
 *
 * Return: nothing
 */

void f_sub(stack_t **head, unsigned int counter)
{
	stack_t *temp;
	int sub, node;

	temp = *head;
	for (node = 0; temp != NULL; node++)
		temp = temp->next;
	if (node < 2)
	{
		fprintf(stderr, "L%d: can't sub, stack too short\n", counter);
		fclose(bus.file);
		free(bus.content);
		free_stack(*head);
		exit(EXIT_FAILURE);
	}
	temp = *head;
	sub = temp->next->n - temp->n;
	temp->next->n = sub;
	*head = temp->next;
	free(temp);
}

